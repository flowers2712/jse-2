package cmm02.op;

public class No01_DanHangDemo {
	public static void main(String[] args) {
	//키워드			메소드명 			바디
		/*
		 * 선언은 키워드+식별자+바디
		 * 바디내부의 알고리즘을 정의 라고한다.
		 * 
		 * */
		int i; // 변수의 선언
		i = 5; // 초기화, 할당
		
		i = i + 1;
		System.out.println("i = i + 5 의 값 : "+ i);
		
		int j = 5;
		j++;
		System.out.println("j++ 의 값 : "+j);
		
		int k = 5;
		++k;
		System.out.println("++k 의 값 : "+k);
		
		
		
		
	}
}

