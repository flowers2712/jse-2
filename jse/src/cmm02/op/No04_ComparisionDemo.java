package cmm02.op;

public class No04_ComparisionDemo {
	public static void main(String[] args) {
		// 키워드 식별자 바디
		int val01 = 1, val02 = 2; // val선언 및 변수초기화
		
		if(val01 == val02){
			System.out.println("val01 == val02");
		}
		if(val01 != val02){
			System.out.println("val01 != val02");
		}
		if(val01 > val02){
			System.out.println("val01 > val02");
		}
		if(val01 < val02){
			System.out.println("val01 < val02");
		}
		/*
		 * 참인것만 출력.
		 * if 이후의 ()<condition값의 조건이 일치하면 if문 동작
		 * 일치하지 않다면 넘어간다.
		 * 
		 * */
	}
}
